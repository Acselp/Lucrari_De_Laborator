    <p>A fost odată, când a fost, că, dacă n-ar fi fost, nu s-ar povesti.
        Noi nu suntem de pe când poveștile, ci suntem mai dincoace cu vro două-trei zile, de pe când se potcovea purecele cu nouăzeci și nouă de ocă de fer la un picior și tot i se părea că-i ușor.
    <p>Cică era odată un om însurat, și omul acela trăia la un loc cu soacră-sa. Nevasta lui, care avea copil de țâță, era cam proastă; dar și soacră-sa nu era tocmai hâtră.
        Întru una din zile, omul nostru iese de-acasă după trebi, ca fiecare om. Nevasta lui, după ce-și scăldă copilul, îl înfășă și-i dete țâță, îl puse în albie lângă sobă, căci era iarnă; apoi îl legănă și-l dezmerdă, până ce-l adormi. După ce-l adormi, stătu ea puțin pe gânduri ș-apoi începu a se boci cât îi lua gura: "Aulio! copilașul meu, copilașul meu!"

        Mama ei, care torcea după horn, cuprinsă de spaimă, zvârli fusul din mână și furca din brâu cât colo și, sărind fără sine, o întrebă cu spaimă:

        <br>&emsp; — Ce ai, draga mamei, ce-ți este?!

        <br>&emsp; — Mamă, mamă! Copilul meu are să moară!

        <br>&emsp; — Când și cum?

        <br>&emsp; — Iată cum. Vezi drobul cel de sare pe horn?

        <br>&emsp; — Îl văd. Și?

        <br>&emsp; — De s-a sui mâța, are să-l trântească drept în capul copilului și să mi-l omoare!

        <br>&emsp; — Vai de mine și de mine, că bine zici, fata mea; se vede că i s-au sfârșit mititelului zilele!

        Și, cu ochii pironiți în drobul de sare de pe horn și cu mânile încleștate, de parcă le legase cineva, începură a-l boci amândouă, ca niște smintite, de clocotea casa. Pe când se sluțeau ele, cum vă spun, numai iaca și tatăl copilului intră pe ușă, flămând și năcăjit ca vai de el.

        <br>&emsp; — Ce este ? Ce v-au găsit, nebunelor?

        Atunci ele, viindu-și puțin în sine, începură a-și șterge lacrămile și a-i povesti cu mare jale despre întâmplarea neîntâmplată. Omul, după ce le ascultă, zise cu mirare:

        <br>&emsp; — Bre! mulți proști am văzut eu în viața mea, dar ca voi n-am mai văzut. Mă... duc în lumea toată! Și de-oiu găsi mai proști decât voi, m-oiu mai întoarce acasă, iar de nu, ba.

        Așa zicând, oftă din greu, ieși din casă, fără să-și ieie ziua bună, și plecă supărat și amărât ca vai de om!
    </p>

    <p>Și mergând el bezmetic, fără să știe unde se duce, după o bucată de vreme, oprindu-se într-un loc, i se întâmplă iar să vadă ceva ce nu mai văzuse: un om ținea puțin un oboroc deșert cu gura spre soare, apoi răpede-l înșfăca și intra cu dânsul într-un bordeiu; pe urmă iar ieșea, îl punea iar cu gura la soare, și tot așa făcea... Drumețul nostru, nedumerit, zise:
        <img src="./img/galeatacusoare.png" alt="Galeata cu soare" class="img_mrgn_l0 float_left">
        <br>&emsp; — Bună ziua, om bun!

        <br>&emsp; — Mulțămesc dumitale, prietene!

        <br>&emsp; — Da' ce faci aici?

        <br>&emsp; — Ia, mă trudesc de vro două-trei zile să car pocitul ist de soare în bordeiu, ca să am lumină, și nici că-l pot...

        <br>&emsp; — Bre, ce trudă! zise drumețul. N-ai vrun topor la îndămână?

        <br>&emsp; — Ba am.

        <br>&emsp; — Ie-l de coadă, sparge ici, și soarele va intra singur înlăuntru.

        Îndată făcu așa, și lumina soarelui întră în bordeiu.

        <br>&emsp; — Mare minune, om bun, zise gazda. De nu te-aducea Dumnezeu pe la noi, eram să îmbătrânesc cărând soarele cu oborocul.

        "Încă un tont", zise drumețul în sine și plecă.
    </p>


    <p>Și mergând el tot înainte, peste câtva timp ajunse într-un sat și, din întâmplare, se opri la casa unui om. Omul de gazdă, fiind rotar, își lucrase un car și-l înjghebase, în casă, în toată întregimea lui; ș-acum, voind să-l scoată afară, trăgea de proțap cu toată puterea, dar carul nu ieșea. Știți pentru ce? Așa: ușile era mai strâmte decât carul. Rotarul voia acum să taie ușorii, spre a scoate carul. Noroc însă că drumețul l-a învățat să-l desfacă în toate părțile lui, să le scoată pe rând afară ș-apoi iarăși să-l înjghebe la loc.
        <br>&emsp; — Foarte mulțămesc, om bun, zise gazda; bine m-ai învățat! Ia uită-te dumneta! Era să dărâm bunătate de casă din pricina carului...
        <img src="./img/nucifurca.png" alt="Nucile" class="float_right img_mrgn_r0">
        De aici, drumețul nostru, mai numărând un nătărău, merse tot înainte, până ce ajunse iară la o casă. Acolo, ce să vadă! Un om, cu-n țăpoiu în mână, voia să arunce niște nuci din tindă în pod.

        "Din ce în ce dau peste dobitoci", zise drumețul în sine.

        <br>&emsp; — Da' ce te frămânți așa, om bun?

        <br>&emsp; — Ia, vreu să zvârl niște nuci în pod, și țăpoiul ista, bată-l scârba să-l bată, nu-i nici de-o treabă...

        <br>&emsp; — Că degeaba te trudești, nene! Poți să-l blastămi cât l-ei blăstăma, habar n-are țăpoiul de scârbă. Ai un oboroc?

        <br>&emsp; — Da' cum să n-am?!

        <br>&emsp; — Pune nucile într-însul, ie-l pe umăr și suie-le frumușel în pod; țăpoiul e pentru paie și fân, iar nu pentru nuci.

        Omul ascultă, și treaba se făcu îndată. Drumețul nu zăbovi nici aici mult, ci plecă, mai numărând și alt neghiob.
    </p>

    <p>Apoi, de aici merse mai departe, până ce ajunsese ca să mai vadă aiurea și altă năzbâtie. Un om legase o vacă cu funia de gât și, suindu-se pe-o șură, unde avea aruncat oleacă de fân, trăgea din răsputeri de funie, să urce vaca pe șură. Vaca răgea cumplit, și el nu mai putea de ostenit...

        <br>&emsp; — Mă omule! zise drumețul, făcându-și cruce; dar ce vrei să faci?

        <br>&emsp; — Ce să fac, mă-ntrebi? Da' nu vezi?

        <br>&emsp; — Ba văd, numai nu pricep.

        <br>&emsp; — Ia, hăramul ista e hâmisit de foame și nu vre' nici în ruptul capului să vie după mine sus, pe iastă șură, să mănânce fân...

        <br>&emsp; — Stai puțin, creștine, că spânzuri vaca! Ia fânul și-l dă jos la vacă!

        <br>&emsp; — Da' nu s-a irosi?...

        <br>&emsp; — Nu fi scump la tărâțe și ieftin la făină.

        Atunci omul ascultă și vaca scăpă cu viață.

        <br>&emsp; — Bine m-ai învățat, om bun! Pentr-un lucru de nimica eram cât pe ce să-mi gâtui vaca!
    </p>

    <p>
        <img src="./img/acasa.png" alt="Acasa" class="img_mrgn_l0 float_left" width="300px">
        Așa, drumețul nostru, mirându-se și de această mare prostie, zise în sine: "Mâța tot s-ar fi putut întâmpla să deie drobul de sare jos de pe horn; dar să cari soarele în casă cu oborocul, să arunci nucile în pod cu țăpoiul și să tragi vaca pe șură, la fân, n-am mai gândit!"

        Apoi drumețul se întoarse acasă și petrecu lângă ai săi, pe cari-i socoti mai cu duh decât pe cei ce văzuse în călătoria sa.
    </p>

    <p>Ș-am încălecat pe-o șa, ș-am spus povestea așa.

        Ș-am încălecat pe-o roată, ș-am spus-o toată.

        Ș-am încălecat pe-o căpșună, și v-am spus, oameni buni, o mare minciună!!</p>
