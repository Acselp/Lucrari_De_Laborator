    <div class="indent-30px">
        <p>Prostia omenească de Ion Creangă spune povestea unui om care locuia împreună cu nevasta, soacra și copilul.</p>
        <p>Într-o zi, omul plecat se întoarce și le găsește pe cele două femei plângând din pricina unui eveniment ce nu avusese loc, pisica s-ar fi putut sui pe horn și să dărâme drobul de sare peste micuț. Bărbatul, supărat din cauza prostiei și a faptului că niciuna nu a mutat drobul, a plecat în lume cu promisiunea că se va întoarce dacă va vedea prostie mai mare.</p>
        <p>Pe drum, călătorul a întâlnit mai mai mulți oameni pe care i-a ajutat.</p>
    </div>

    <br>

    <ol>
        <li>Primul om se străduia să prindă soarele cu o găleată pentru a avea lumină în bordei, acestuia i-a făcut o gaură în acoperiș.</li>
        <li>Al doilea om și-a făcut un car mare în casă care nu putea fi scos, așa că s-a gândit să taie ușa. Lui i-a sugerat să dezasambleze carul pe bucăți.</li>
        <li>Al treilea om încerca din răsputeri să mute niște nuci cu furca, dar a aflat că trebuie să folosească o baniță.</li>
        <li>Ultimul om încercă să ridice vaca legată de gât cu o funie pe șură pentru a-i da de mâncare. Văzând și această pățanie, călătorul pleacă acasă după ce îl ajută pe om spunându-i să aducă fânul la vacă.</li>
    </ol>
